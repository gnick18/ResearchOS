/* ------------------------------------------------------- pinned reference set */

/**
 * One pinned statistic: which engine quantity it is, the reference value, which
 * oracle produced it, and a tolerance band. `metric` is the human label shown on
 * the page. `kind` drives how run.ts reads the value out of the engine result.
 *
 * `tol` is the maximum |our - reference| that still counts as agreement. Most are
 * tight (a faithful match to floating point). A couple are wider and carry a
 * `difference` note explaining a genuine, documented method difference (e.g.
 * exact vs normal-approximation p-values) that is expected, not a bug.
 */
export interface StatPin {
  /** Stable case id, e.g. "unpaired_welch_t". */
  id: string;
  /** Human label, e.g. "Welch t-test, t statistic". */
  metric: string;
  /** The reference value from the oracle tool. */
  reference: number;
  /** Oracle id (see oracles.ts): scipy | statsmodels | lifelines. */
  oracleId: string;
  /** Pass tolerance (|delta| at or below this is agreement). */
  tol: number;
  /** Warn tolerance (|delta| above pass, at/below this, is a documented diff). */
  warn: number;
  /** Unit label for the delta. */
  unit: string;
  /** When set, this is a documented method difference, surfaced on the page. */
  difference?: string;
}

