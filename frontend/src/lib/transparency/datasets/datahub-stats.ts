/**
 * Data Hub statistics validation cases for the transparency page.
 *
 * Where the bioinformatics datasets check ResearchOS against Biopython / primer3,
 * these cases check the Data Hub statistics engine (the free GraphPad Prism style
 * analysis core in frontend/src/lib/datahub/engine) against the tools working
 * scientists already trust: scipy.stats, statsmodels, and lifelines.
 *
 * THE SINGLE FIXED DATASET. Every test below runs on the SAME small, hardcoded
 * dataset defined here. The identical arrays are mirrored verbatim in the
 * committed generator `frontend/scripts/gen-datahub-stats-golden.py`, which feeds
 * them to scipy / statsmodels / lifelines and prints the reference values pinned
 * below. A reader can paste the same arrays into scipy and reproduce every number
 * by hand. run.ts runs OUR engine on these arrays and classifies the agreement.
 *
 * THE STANDING RULE (docs/datahub/STATS_VALIDATION.md). Every new Data Hub
 * statistical test must add (a) a scipy/statsmodels/R reference value generated by
 * gen-datahub-stats-golden.py and (b) a pinned comparison here, before it ships.
 *
 * No network and no Python at runtime: the reference values are embedded
 * constants so `buildTransparencyReport()` and its gate test stay pure and
 * deterministic. No em-dashes, no emojis, no mid-sentence colons.
 */

